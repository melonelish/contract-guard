def _promote_missing_clauses_to_risks(report: dict[str, Any]) -> None:
    """Phase 3f: ensure every missing_clause also has a corresponding high-risk in risks.

    The LLM sometimes lists missing_clauses but does not create corresponding risk
    items. This function bridges that gap by injecting high-risk entries for any
    missing clause that doesn't already have a matching risk.
    """
    missing_clauses = report.get("missing_clauses", [])
    if not missing_clauses:
        return

    risks = report.get("risks", [])
    existing_risk_texts = set()
    for r in risks:
        # Collect text fingerprints for dedup
        existing_risk_texts.add(r.get("risk_category", ""))
        existing_risk_texts.add(r.get("clause_code", ""))
        existing_risk_texts.add(r.get("original_text", "")[:30] if r.get("original_text") else "")

    new_risks: list[dict[str, Any]] = []
    for i, mc in enumerate(missing_clauses):
        name = mc.get("name", "")
        reason = mc.get("reason", "")
        if not name:
            continue

        # Check if a risk already covers this missing clause
        # by checking if the clause name or a close variant appears in any risk's
        # category, text, or description
        already_covered = False
        name_lower = name.lower()
        for r in risks:
            cat = r.get("risk_category", "")
            text = " ".join([
                r.get("original_text", ""),
                r.get("legal_analysis", ""),
                r.get("plain_explanation", ""),
            ]).lower()
            # Check if the missing clause name is substantially covered
            if name in cat or cat in name:
                already_covered = True
                break
            # Check for keyword overlap (at least 2 chars matching)
            if len(name) >= 3 and name in text:
                already_covered = True
                break

        if already_covered:
            continue

        # Determine risk_category from the missing clause name
        risk_category = "条款缺失"  # default for missing clauses
        for keyword, category in _MISSING_CLAUSE_CATEGORY_MAP.items():
            if keyword in name:
                risk_category = category
                break

        new_risk: dict[str, Any] = {
            "clause_id": f"missing_{i + 1:03d}",
            "clause_code": "缺失条款",
            "risk_level": "high",
            "risk_category": risk_category,
            "original_text": f"（合同中未发现{name}）",
            "legal_analysis": reason or f"合同缺少{name}，可能导致法律风险。",
            "legal_basis": "依据不足，基于法理分析",
            "basis_excerpt": "",
            "basis_source": "",
            "plain_explanation": f"合同中缺少{name}，这是一个重要的保护条款。",
            "suggested_revision": f"建议增加{name}。",
            "confidence": 0.8,
        }
        new_risks.append(new_risk)

    if new_risks:
        risks.extend(new_risks)
        report["risks"] = risks


