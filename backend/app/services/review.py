def validate_report_schema(report: dict[str, Any]) -> tuple[bool, str]:
    """Validate that the LLM output conforms to the expected report schema.

    Returns (is_valid, error_message).
    """
    required_top = ["summary", "risks"]
    for field in required_top:
        if field not in report:
            return False, f"Missing required field: {field}"

    summary = report.get("summary")
    if not isinstance(summary, dict):
        return False, "summary must be a dict"
    for key in ["total_risks", "high", "medium", "low"]:
        if key not in summary:
            return False, f"summary missing key: {key}"

    risks = report.get("risks")
    if not isinstance(risks, list):
        return False, "risks must be a list"

    for i, risk in enumerate(risks):
        required_risk = ["clause_id", "risk_level", "risk_category"]
        for field in required_risk:
            if field not in risk:
                return False, f"risks[{i}] missing field: {field}"
        if risk["risk_level"] not in ("high", "medium", "low"):
            return False, f"risks[{i}].risk_level invalid: {risk['risk_level']}"
        # Ensure confidence is a float between 0 and 1
        if "confidence" in risk:
            try:
                risk["confidence"] = max(0.0, min(1.0, float(risk["confidence"])))
            except (ValueError, TypeError):
                risk["confidence"] = 0.5
        for field in ["legal_basis", "basis_excerpt", "basis_source"]:
            if field in risk and risk[field] is None:
                risk[field] = ""
            if field in risk and not isinstance(risk[field], str):
                return False, f"risks[{i}].{field} must be a string"

    return True, ""


def clean_report(report: dict[str, Any]) -> dict[str, Any]:
    """Apply minimal cleaning/defaults to a validated report."""
    # Ensure summary counts are consistent
    risks = report.get("risks", [])
    high = sum(1 for r in risks if r.get("risk_level") == "high")
    medium = sum(1 for r in risks if r.get("risk_level") == "medium")
    low = sum(1 for r in risks if r.get("risk_level") == "low")
    report["summary"] = {
        "total_risks": len(risks),
        "high": high,
        "medium": medium,
        "low": low,
    }

    # Ensure lists exist
    report.setdefault("contradictions", [])
    report.setdefault("missing_clauses", [])

    # Ensure each risk has all expected fields with defaults
    for risk in risks:
        risk.setdefault("clause_code", risk.get("clause_id", ""))
        risk.setdefault("original_text", "")
        risk.setdefault("legal_analysis", "")
        risk.setdefault("plain_explanation", "")
        risk.setdefault("suggested_revision", "")
        risk.setdefault("confidence", 0.5)
        # Phase 3: legal basis fields
        risk.setdefault("legal_basis", "依据不足，基于法理分析")
        risk.setdefault("basis_excerpt", "")
        risk.setdefault("basis_source", "")

    # Inject disclaimer
