async def get_latest_review_for_contract(
    session: AsyncSession, contract_id: uuid.UUID,
) -> Review | None:
    """Return the most recent Review for a contract, or None."""
    stmt = (
        select(Review)
        .where(Review.contract_id == contract_id)
        .order_by(Review.created_at.desc())
        .limit(1)
    )
    return (await session.scalars(stmt)).first()


async def get_latest_reviews_for_contracts(
    session: AsyncSession, contract_ids: list[uuid.UUID],
) -> dict[uuid.UUID, Review]:
    """Return the latest review for each contract_id in the given list."""
    if not contract_ids:
        return {}
    subq = (
        select(
            Review.contract_id,
            func.max(Review.created_at).label("max_created"),
        )
        .where(Review.contract_id.in_(contract_ids))
        .group_by(Review.contract_id)
        .subquery()
    )
    stmt = (
        select(Review)
        .join(
            subq,
            (Review.contract_id == subq.c.contract_id)
            & (Review.created_at == subq.c.max_created),
        )
    )
    rows = (await session.scalars(stmt)).all()
    return {r.contract_id: r for r in rows}


async def create_review(
    session: AsyncSession,
    user: User,
    contract: Contract,
    use_draft_content: bool = False,
) -> Review:
    """Create a new review, blocking if one is already running.

    Args:
        use_draft_content: If True, mark this review as reviewing draft content.
    """
    from app.schemas.errors import ErrorCode

    lock_stmt = select(Contract.id).where(Contract.id == contract.id).with_for_update()
    await session.execute(lock_stmt)

    existing = await get_latest_review_for_contract(session, contract.id)
    if existing and existing.status in REVIEW_RUNNING_STATUSES:
        raise ReviewServiceError(
            ErrorCode.REVIEW_IN_PROGRESS,
            "review already in progress",
        )

    review = Review(
        contract_id=contract.id,
        tenant_id=user.tenant_id,
        user_id=user.id,
        status="queued",
        progress=0,
        schema_version="1.0",
        reviewed_draft=use_draft_content,
    )
    session.add(review)
    await session.commit()
    await session.refresh(review)
    return review


async def get_review(session: AsyncSession, review_id: uuid.UUID) -> Review | None:
    return await session.get(Review, review_id)


async def get_review_tenant_scoped(
    session: AsyncSession, review_id: uuid.UUID, tenant_id: uuid.UUID,
) -> Review | None:
    stmt = (
        select(Review)
        .where(Review.id == review_id, Review.tenant_id == tenant_id)
        .options(selectinload(Review.contract))
    )
    return (await session.scalars(stmt)).first()


