def _upgrade_agency_risks(report: dict[str, Any]) -> None:
    """Phase 3e.2: upgrade medium risks to high for core agency categories.

    Only upgrades when:
    1. risk_category is a core agency topic (佣金计算/培训/客户归属/销售指标/付款条件)
    2. text contains keywords indicating significant business risk or imbalance
    3. current risk_level is medium (never upgrades low → high)
    """
    for risk in report.get("risks", []):
        if risk.get("risk_level") != "medium":
            continue

        category = risk.get("risk_category", "")
        text = " ".join([
            risk.get("original_text", ""),
            risk.get("legal_analysis", ""),
            risk.get("plain_explanation", ""),
            risk.get("suggested_revision", ""),
        ])

        for rule in _AGENCY_UPGRADE_RULES:
            if category == rule["category"]:
                if any(kw in text for kw in rule["keywords"]):
                    risk["risk_level"] = "high"
                break


