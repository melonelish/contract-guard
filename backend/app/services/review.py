async def run_real_review(
    review_id: uuid.UUID,
    contract_title: str | None,
    contract_file_url: str,
    contract_file_type: str,
    contract_id: uuid.UUID | None = None,
    use_draft_content: bool = False,
) -> None:
    """Background coroutine that runs the real LLM review pipeline.

    Stages:
    1. parsing: Extract text from document
    2. analyzing: Send to LLM for analysis
    3. reporting: Parse and structure LLM response
    4. validating: Validate report schema
    5. completed / failed

    Phase 4: Each stage publishes a Redis Pub/Sub event for WebSocket push.
    """
    from app.db.session import get_session_factory

    task_id = str(review_id)  # MVP: task_id == review_id

    async with get_session_factory()() as session:
        review = await session.get(Review, review_id)
        if not review:
            return

        # --- Stage 1: Parsing ---
        review.status = "parsing"
        review.current_stage = "parsing"
        review.progress = 10
        review.started_at = datetime.utcnow()
        await session.commit()

        await _publish_stage(review_id, task_id, "parsing", 10, "开始文档解析")

        try:
            if use_draft_content:
                # Phase v12: Review draft content instead of original file
                if not contract_id:
                    await _mark_failed(session, review, "草稿审查失败: 未提供 contract_id")
                    return

                contract = await session.get(Contract, contract_id)
                if not contract:
                    await _mark_failed(session, review, "草稿审查失败: 找不到合同")
                    return

                if not contract.draft_content or not contract.draft_content.strip():
                    await _mark_failed(
                        session,
                        review,
                        "草稿审查失败: 草稿内容为空，无法审查。请先在编辑模式中保存草稿内容。"
                    )
                    return

                # Convert TipTap HTML to plain text
                contract_text = _html_to_text(contract.draft_content)
                logger.info("review.draft_extracted", extra={
                    "review_id": str(review_id),
                    "draft_length": len(contract.draft_content),
                    "text_length": len(contract_text),
                })
            else:
                # Original flow: extract from file
                contract_text = await _extract_contract_text(contract_file_url, contract_file_type)
        except Exception as exc:
            await _mark_failed(session, review, f"文档解析失败: {exc}")
            return

        review.progress = 25
        await session.commit()

        await _publish_stage(review_id, task_id, "parsing", 25, "文档解析完成")

        # --- Stage 1.5: RAG Retrieval ---
        rag_results = []
        rag_enabled = False
        try:
            from app.services.rag import (
                extract_search_queries,
                format_law_context,
                search_law_articles,
            )
            queries = extract_search_queries(contract_text)
            if queries:
                rag_results = await search_law_articles(session, queries, top_k=10)
                rag_enabled = len(rag_results) > 0
                logger.info("rag.retrieval", extra={
                    "review_id": str(review_id),
                    "queries": queries,
                    "hits": len(rag_results),
                })
        except Exception as exc:
            logger.warning("rag.retrieval_failed", extra={
                "review_id": str(review_id),
                "error": str(exc),
            })
            # RAG failure is non-fatal — degrade to model-only analysis

        # Format law context for LLM
        law_context = ""
        if rag_results:
            law_context = format_law_context(
                rag_results, max_chars=RAG_CONTEXT_MAX_CHARS, contract_title=contract_title or "",
            )

        # --- Stage 2: Analyzing (LLM call) ---
        review.status = "analyzing"
        review.current_stage = "analyzing"
        review.progress = 30
        await session.commit()

        await _publish_stage(review_id, task_id, "analyzing", 30, "正在分析合同条款")

        # Truncate extremely long contracts to stay within token limits
        max_chars = 60000  # ~15K tokens
        if len(contract_text) > max_chars:
            contract_text = contract_text[:max_chars] + "\n\n[... 合同内容过长，已截断 ...]"

        user_prompt = USER_PROMPT_TEMPLATE.format(
            title=contract_title or "未命名合同",
            contract_text=contract_text,
            law_context=law_context,
        )

        final_response: dict[str, Any] | None = None

        heartbeat_task: asyncio.Task[None] | None = None
        try:
            from app.llm import llm_chat
            heartbeat_task = asyncio.create_task(_analyzing_progress_heartbeat(review_id, task_id))
            llm_response = await asyncio.wait_for(
                llm_chat(
                    model="mimo2.5",
                    system_prompt=SYSTEM_PROMPT,
                    user_message=user_prompt,
                    temperature=0.0,
                    max_tokens=LLM_MAX_TOKENS,
                    response_format={"type": "json_object"},
                ),
                timeout=LLM_CALL_TIMEOUT_SECONDS,
            )
            final_response = llm_response
        except TimeoutError:
            logger.error(
                "review.llm_timeout",
                extra={"review_id": str(review_id), "timeout": LLM_CALL_TIMEOUT_SECONDS},
            )
            await _mark_failed(
                session,
                review,
                f"LLM 调用超时，超过 {LLM_CALL_TIMEOUT_SECONDS} 秒仍未返回",
            )
            return
        except Exception as exc:
            logger.error(
                "review.llm_failed",
                extra={"review_id": str(review_id), "error": str(exc)},
            )
            await _mark_failed(session, review, f"LLM 调用失败: {exc}")
            return
        finally:
            if heartbeat_task is not None:
                heartbeat_task.cancel()
                with contextlib.suppress(asyncio.CancelledError):
                    await heartbeat_task

        review.progress = 70
        await session.commit()

        # --- Stage 3: Reporting (parse LLM output) ---
        review.status = "reporting"
        review.current_stage = "reporting"
        review.progress = 75
        await session.commit()

        await _publish_stage(review_id, task_id, "reporting", 75, "正在生成审查报告")

        # Phase 3e: Handle empty/None content from LLM
        raw_content = llm_response.get("content", "")
        if not raw_content or not raw_content.strip():
            logger.error(
                "review.empty_llm_content",
                extra={
                    "review_id": str(review_id),
                    "content_length": len(raw_content) if raw_content else 0,
                },
            )
            # Retry once with simplified prompt
            try:
                retry_response = await llm_chat(
                    model="mimo2.5",
                    system_prompt=SYSTEM_PROMPT,
                    user_message=user_prompt[:4000] + "\n\n请务必输出合法 JSON，不要输出空内容。",
                    temperature=0.0,
                    max_tokens=LLM_MAX_TOKENS,
                    response_format={"type": "json_object"},
                )
                raw_content = retry_response.get("content", "")
                if not raw_content or not raw_content.strip():
                    await _mark_failed(
                        session,
                        review,
                        "LLM 返回空内容（重试后仍为空）",
                    )
                    return
                llm_response = retry_response
            except Exception as retry_exc:
                await _mark_failed(
                    session,
                    review,
                    f"LLM 返回空内容，重试失败: {retry_exc}",
                )
                return

        try:
            report_data = _parse_llm_report_content(raw_content)
        except json.JSONDecodeError as exc:
            logger.error(
                "review.json_parse_failed",
                extra={
                    "review_id": str(review_id),
                    "error": str(exc),
                    "raw_content_length": len(raw_content),
                    "raw_content_preview": raw_content[:500],
                },
            )
            try:
                report_data = await _repair_llm_json(raw_content)
            except Exception as repair_exc:
                await _mark_failed(
                    session,
                    review,
                    f"JSON解析失败: {exc}; 修复失败: {repair_exc}; "
                    f"raw_len={len(raw_content)}; preview={raw_content[:200]}",
                )
                return

        review.progress = 85
        await session.commit()

        # --- Stage 4: Validating ---
        review.status = "validating"
        review.current_stage = "validating"
        review.progress = 90
        await session.commit()

        await _publish_stage(review_id, task_id, "validating", 90, "正在校验审查报告")

        is_valid, validation_error = validate_report_schema(report_data)
        if not is_valid:
            logger.warning("review.schema_invalid", extra={
                "review_id": str(review_id),
                "error": validation_error,
            })
            initial_report_data = report_data
            # Try once more with a simpler prompt
            try:
                retry_response = await llm_chat(
                    model="mimo2.5",
                    system_prompt=SYSTEM_PROMPT,
                    user_message=user_prompt[:4000] + "\n\n请务必输出合法 JSON。",
                    temperature=0.0,
                    max_tokens=LLM_MAX_TOKENS,
                    response_format={"type": "json_object"},
                )
                retry_raw = retry_response["content"]
                try:
                    retry_report_data = _parse_llm_report_content(retry_raw)
                except json.JSONDecodeError:
                    retry_report_data = await _repair_llm_json(retry_raw)
                is_valid, validation_error = validate_report_schema(retry_report_data)
                if not is_valid:
                    report_data, fallback_source = _select_schema_fallback_report(
                        initial_report_data,
                        retry_report_data,
                    )
                    if report_data is None:
                        await _mark_failed(
                            session,
                            review,
                            f"schema不合规: {validation_error}; "
                            f"retry_raw_len={len(retry_raw)}",
                        )
                        return
                    logger.warning("review.schema_retry_fallback", extra={
                        "review_id": str(review_id),
                        "validation_error": validation_error,
                        "fallback_source": fallback_source,
                    })
                else:
                    report_data = retry_report_data
                    final_response = retry_response
            except Exception as retry_exc:
                # Retry call failed — fall back to initial result if usable
                if not initial_report_data.get("risks"):
                    await _mark_failed(
                        session,
                        review,
                        f"schema重试失败: {retry_exc}",
                    )
                    return
                report_data = initial_report_data
                logger.warning("review.schema_retry_exception_fallback", extra={
                    "review_id": str(review_id),
                    "retry_error": str(retry_exc),
                })

        # Clean and finalize report
        report_data = clean_report(report_data)

        # Phase 3e.2: Post-process risk categories + upgrade for agency contracts
        if contract_title and any(kw in contract_title for kw in ["代理", "经销", "销售"]):
            _reclassify_agency_risks(report_data)
            _upgrade_agency_risks(report_data)

        # Phase 3f: Promote missing clauses to high-risk items
        _promote_missing_clauses_to_risks(report_data)

        # Recompute summary counts after all post-processing
        _update_summary_counts(report_data)

        if final_response is not None:
            report_data["llm_meta"] = build_llm_meta(final_response)

        # Phase 3: legal basis must be grounded in retrieved law articles
        if rag_results:
            for risk in report_data.get("risks", []):
                _ground_risk_legal_basis(risk, rag_results, contract_title or "")

        # Add RAG metadata
        report_data["rag_meta"] = {
            "enabled": rag_enabled,
            "hit_count": len(rag_results),
            "mode": "rag_enhanced" if rag_enabled else "model_only",
            "queries": queries if rag_enabled else [],
        }

        # --- Stage 5: Completed ---
        review.report_json = report_data
        review.status = "completed"
        review.progress = 100
        review.current_stage = None
        review.completed_at = datetime.utcnow()
        await session.commit()

        # Phase 4: publish completion event
        duration_sec = int((review.completed_at - review.started_at).total_seconds()) if review.started_at else 0
        await _publish_complete(review_id, task_id, report_data.get("summary", {}), duration_sec)

        logger.info("review.completed", extra={
            "review_id": str(review_id),
            "total_risks": report_data["summary"]["total_risks"],
            "model": llm_response["model"],
            "prompt_tokens": llm_response["usage"]["prompt_tokens"],
            "completion_tokens": llm_response["usage"]["completion_tokens"],
        })


