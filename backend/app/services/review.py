async def recover_stuck_reviews(session: AsyncSession) -> int:
    """On startup, mark any stuck running reviews as failed."""
    stmt = select(Review).where(Review.status.in_(REVIEW_RUNNING_STATUSES))
    stuck = list((await session.scalars(stmt)).all())
    for review in stuck:
        review.status = "failed"
        review.error_detail = "server restarted while review was in progress"
        review.completed_at = datetime.utcnow()
    if stuck:
        await session.commit()
    return len(stuck)
